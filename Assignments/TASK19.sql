-- 2) Procedure for withdraw logic
CREATE OR REPLACE PROCEDURE OMS_DEV.BRONZE.WITHDRAW(
    P_CID NUMBER,
    P_AMOUNT NUMBER
)
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
DECLARE
    V_BALANCE NUMBER;
BEGIN
    SELECT AMOUNT INTO :V_BALANCE FROM OMS_DEV.BRONZE.CUSTOMERS WHERE CID = :P_CID;

    IF (:V_BALANCE < :P_AMOUNT) THEN
        RETURN 'Insufficient balance. Current balance: ' || :V_BALANCE;
    END IF;

    UPDATE OMS_DEV.BRONZE.CUSTOMERS
    SET AMOUNT = AMOUNT - :P_AMOUNT,
        UPDATE_TIME = CURRENT_TIMESTAMP()
    WHERE CID = :P_CID;

    RETURN 'Withdrawal successful. New balance: ' || (:V_BALANCE - :P_AMOUNT);
END;
$$;

CALL OMS_DEV.BRONZE.WITHDRAW(1, 1000);

-- 3) Procedure for deposit logic
CREATE OR REPLACE PROCEDURE OMS_DEV.BRONZE.DEPOSIT(
    P_CID NUMBER,
    P_AMOUNT NUMBER
)
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
DECLARE
    V_BALANCE NUMBER;
BEGIN
    UPDATE OMS_DEV.BRONZE.CUSTOMERS
    SET AMOUNT = AMOUNT + :P_AMOUNT,
        UPDATE_TIME = CURRENT_TIMESTAMP()
    WHERE CID = :P_CID;

    SELECT AMOUNT INTO :V_BALANCE FROM OMS_DEV.BRONZE.CUSTOMERS WHERE CID = :P_CID;

    RETURN 'Deposit successful. New balance: ' || :V_BALANCE;
END;
$$;

CALL OMS_DEV.BRONZE.DEPOSIT(1, 1000);

-- 4) Procedure to display customers based on branch/address
CREATE OR REPLACE PROCEDURE OMS_DEV.BRONZE.GET_CUSTOMERS_BY_ADDRESS(
    P_ADDRESS VARCHAR
)
RETURNS TABLE(CID NUMBER, CNAME VARCHAR, AMOUNT NUMBER, ADDRESS VARCHAR, PHONE VARCHAR)
LANGUAGE SQL
AS
$$
DECLARE
    RES RESULTSET;
BEGIN
    RES := (SELECT CID, CNAME, AMOUNT, ADDRESS, PHONE
            FROM OMS_DEV.BRONZE.CUSTOMERS
            WHERE UPPER(ADDRESS) = UPPER(:P_ADDRESS));
    RETURN TABLE(RES);
END;
$$;

CALL OMS_DEV.BRONZE.GET_CUSTOMERS_BY_ADDRESS('BLR');

-- 5) Procedure to delete inactive customer
CREATE OR REPLACE PROCEDURE OMS_DEV.BRONZE.DELETE_CUSTOMER(
    P_CID NUMBER
)
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
BEGIN
    DELETE FROM OMS_DEV.BRONZE.CUSTOMERS WHERE CID = :P_CID;
    RETURN 'Customer with CID ' || :P_CID || ' deleted successfully';
END;
$$;

