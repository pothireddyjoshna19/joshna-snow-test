-- 1) Procedure to insert values
CREATE OR REPLACE PROCEDURE OMS_DEV.BRONZE.INSERT_CUSTOMER(
    P_CNAME VARCHAR,
    P_AMOUNT NUMBER,
    P_ADDRESS VARCHAR,
    P_PHONE VARCHAR
)
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
BEGIN
    INSERT INTO OMS_DEV.BRONZE.CUSTOMERS (CNAME, AMOUNT, ADDRESS, PHONE)
    VALUES (:P_CNAME, :P_AMOUNT, :P_ADDRESS, :P_PHONE);
    RETURN 'Customer inserted successfully';
END;
$$;

