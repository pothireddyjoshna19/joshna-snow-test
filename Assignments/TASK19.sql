CREATE OR REPLACE PROCEDURE OMS_DEV.BRONZE.MINI_STATEMENT(
    P_CID NUMBER
)
RETURNS TABLE(CID NUMBER, CNAME VARCHAR, AMOUNT NUMBER, ADDRESS VARCHAR, PHONE VARCHAR, CREATE_TIME TIMESTAMP, UPDATE_TIME TIMESTAMP)
